package org.classes.mygolfcard;


public class Stroke {
	private int card_id;
	private int match_id;
	private int player_id;
	private int id;
	private int holeNumber;
	private int hole_id;
	private int strokes;
	private int putts;
	
	/**
	 * @return the card_id
	 */
	public int getCard_id() {
		return card_id;
	}
	
	/**
	 * @param card_id the card_id to set
	 */
	public void setCard_id(int card_id) {
		this.card_id = card_id;
	}
	
	/**
	 * @return the match_id
	 */
	public int getMatch_id() {
		return match_id;
	}
	
	/**
	 * @param match_id the match_id to set
	 */
	public void setMatch_id(int match_id) {
		this.match_id = match_id;
	}
	
	/**
	 * @return the player_id
	 */
	public int getPlayer_id() {
		return player_id;
	}
	
	/**
	 * @param player_id the player_id to set
	 */
	public void setPlayer_id(int player_id) {
		this.player_id = player_id;
	}
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	 * @return the holeNumber
	 */
	public int getHoleNumber() {
		return holeNumber;
	}
	
	/**
	 * @param holeNumber the holeNumber to set
	 */
	public void setHoleNumber(int holeNumber) {
		this.holeNumber = holeNumber;
	}
	
	/**
	 * @return the hole_id
	 */
	public int getHole_id() {
		return hole_id;
	}
	
	/**
	 * @param hole_id the hole_id to set
	 */
	public void setHole_id(int hole_id) {
		this.hole_id = hole_id;
	}
	
	/**
	 * @return the strokes
	 */
	public int getStrokes() {
		return strokes;
	}
	
	/**
	 * @param strokes the strokes to set
	 */
	public void setStrokes(int strokes) {
		this.strokes = strokes;
	}
	
	/**
	 * @return the putts
	 */
	public int getPutts() {
		return putts;
	}
	
	/**
	 * @param putts the putts to set
	 */
	public void setPutts(int putts) {
		this.putts = putts;
	}
	
	
	
}